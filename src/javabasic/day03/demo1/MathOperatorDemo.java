package javabasic.day03.demo1;

/**
 * @author DataMoon
 */
public class MathOperatorDemo {
    public static void main(String[] args) {
        int a = 15;
        int b = 2;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
        System.out.println(a / b * 1.0);
        System.out.println(1.0 * a / b);
        System.out.println((double) (a / b));
        System.out.println((double) a / b);
    }
}
