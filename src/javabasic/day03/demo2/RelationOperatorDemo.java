public class RelationOperatorDemo {
    public static void main(String[] args) {
        int a = 15;
        int b = 2;
        System.out.println(a = b);
        System.out.println(a == b);

        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println("Hello" instanceof Object);
    }
}
